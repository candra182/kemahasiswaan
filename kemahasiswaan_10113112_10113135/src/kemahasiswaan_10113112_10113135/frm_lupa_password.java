    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        if(txt_pengguna.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, 
                "Data tidak boleh ada yang kosong, Silahkan dilengkapi");
        }
        else
        {
            //cek pengguna ada atau tidak
            try
            {
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(
                     database,
                     user,
                     pass);
                Statement stt = kon.createStatement();
                String SQL    = "select * from t_pengguna where BINARY pengguna='"+txt_pengguna.getText()+"'";
                ResultSet res = stt.executeQuery(SQL);
                //jika pengguna ada maka
                if(res.next())
                {
                    tanya.setText("Pertanyaan");
                    pertanyaan.setText(res.getString(6));
                    txt_jawaban.setEnabled(true);
                    btn_submit.setEnabled(true);
                    txt_jawaban.requestFocus();
                    txt_pengguna.setEnabled(false);
                    btn_cari.setEnabled(false);
                }
                //jika tidak
                else
                {
                    JOptionPane.showMessageDialog(null, "Nama Pengguna Tidak Ditemukan atau Tidak Terdaftar",
                            "Pesan",JOptionPane.ERROR_MESSAGE);
                }
                res.close();
                stt.close();
                kon.close();
            }

            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_cariActionPerformed

