    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        if(txt_jawaban.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, 
                "Data tidak boleh ada yang kosong, Silahkan dilengkapi");
        }
        else
        {
            //cek jawaban pertanyaan keamanan sama atau tidak
            try
            {
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(
                     database,
                     user,
                     pass);
                Statement stt = kon.createStatement();
                String SQL    = "select * from t_pengguna where BINARY pengguna='"+txt_pengguna.getText()+"' AND pertanyaan_keamanan='"+pertanyaan.getText()+"'";
                ResultSet res = stt.executeQuery(SQL);
                //jika pengguna dan pertanyaan keamanan sesuai maka...
                if(res.next())
                {
                    //jika jawaban benar maka...
                    if(res.getString(7).equals(txt_jawaban.getText()))
                    {
                        txt_psw_baru.setEnabled(true);
                        txt_vpsw_baru.setEnabled(true);
                        txt_psw_baru.requestFocus();
                        txt_jawaban.setEnabled(false);
                        btn_submit.setEnabled(false);
                        btn_ubah.setEnabled(true);
                    }
                    //jika jawaban salah maka...
                    else
                    {
                        JOptionPane.showMessageDialog(null,
                        "Jawaban Anda salah","Pesan",
                        JOptionPane.ERROR_MESSAGE);
                        txt_jawaban.requestFocus();
                    }
                }
                //jika tidak
                else
                {
                    JOptionPane.showMessageDialog(null, "Nama Pengguna / Pertanyaan Tidak Ditemukan atau Tidak Terdaftar",
                            "Pesan",JOptionPane.ERROR_MESSAGE);
                }
                res.close();
                stt.close();
                kon.close();
            }

            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        //jika password dan verifikasi password sama
        if (txt_psw_baru.getText().equals(txt_vpsw_baru.getText()))
        {
            //update password
            try
            {
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(
                     database,
                     user,
                     pass);
                Statement stt = kon.createStatement();
                String SQL    = "UPDATE `t_pengguna` set `password` = '"+txt_psw_baru.getText()+"' where pengguna='"+txt_pengguna.getText()+"'";
                stt.executeUpdate(SQL);
                JOptionPane.showMessageDialog(null, "Password berhasil di ubah","Pesan",JOptionPane.INFORMATION_MESSAGE);
                stt.close();
                kon.close();
            }

            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else
        {
            txt_test1.setText("Password Tidak Sama");
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

